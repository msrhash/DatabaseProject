CREATE TABLE Reservation (
    reservation_id NUMBER PRIMARY KEY,
    customer_id NUMBER,
    transport_id NUMBER,
    room_id NUMBER,
    hotel_id NUMBER,
    apt_id NUMBER,
    check_in_date DATE,
    check_out_date DATE,
    reservation_status VARCHAR2(20),
    total_price NUMBER(10, 2),
    CONSTRAINT fk_reservation1 
     FOREIGN KEY (customer_id)
     REFERENCES Customer (customer_id) ON DELETE CASCADE,
    CONSTRAINT fk_reservation2 
     FOREIGN KEY (transport_id)
     REFERENCES Transport (transport_id) ON DELETE CASCADE, 
    CONSTRAINT fk_reservation3 
     FOREIGN KEY (room_id)
     REFERENCES Room (room_id) ON DELETE CASCADE,
     CONSTRAINT fk_reservation4 
     FOREIGN KEY (hotel_id)
     REFERENCES Hotel (hotel_id) ON DELETE CASCADE,
     CONSTRAINT fk_reservation5 
     FOREIGN KEY (apt_id)
     REFERENCES Apartment (apt_id) ON DELETE CASCADE
);


