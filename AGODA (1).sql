//parents
CREATE TABLE Customer (
    customer_id        NUMBER(10) PRIMARY KEY,
    first_name         VARCHAR2(50) NOT NULL,
    last_name          VARCHAR2(50) NOT NULL,
    email              VARCHAR2(100) UNIQUE NOT NULL,
    phone_number       VARCHAR2(20),
    password           VARCHAR2(100) NOT NULL,
    date_of_birth      DATE,
    country            VARCHAR2(50),
    membership_status  VARCHAR2(20) DEFAULT 'Regular' 
                      CHECK (membership_status IN ('Regular', 'Silver', 'Gold', 'Platinum')),
    registration_date  DATE DEFAULT SYSDATE
);

CREATE TABLE Hotel (
    hotel_id NUMBER PRIMARY KEY,
    hotel_name VARCHAR2(50) NOT NULL, 
    address VARCHAR2(500) NOT NULL,
    phone_number VARCHAR2(10),
    email VARCHAR2(200) UNIQUE NOT NULL,
    manager_name VARCHAR2(50),
    num_of_rooms NUMBER,
    status VARCHAR2(20),
    rating NUMBER(2, 1),
    website CLOB

);

CREATE TABLE FLIGHTS(
    flight_no NUMBER(10) PRIMARY KEY,
    flight_name VARCHAR2(50),
    time_depart DATE ,
    time_arrived DATE,
    date_depart DATE,
    date_arrived DATE,
    origin VARCHAR2(50),
    destination VARCHAR2 (50),
    flight_status VARCHAR2(50),
    seat_capacity VARCHAR2 (5)
);

CREATE TABLE APARTMENT(
    apt_id NUMBER(10)PRIMARY KEY,
    landlord_name VARCHAR2(50),
    apt_address VARCHAR2(200),
    unitNo NUMBER(5),
    city VARCHAR2(20),
    rent_per_month NUMBER(6,2),
    num_of_rooms NUMBER(2),
    max_guests NUMBER(2),
    landlord_phone_no VARCHAR2(20),
    utilities_included VARCHAR2(20)
);

CREATE TABLE TRAVEL_PACKAGE (
    package_id          NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hotel_id            NUMBER(10) NOT NULL,
    package_name        VARCHAR2(20) NOT NULL,
    destination_name    VARCHAR2(20),
    duration            NUMBER(3),
    price               NUMBER(6,2) CHECK (price >= 0) NOT NULL,
    Included_activities CLOB,
    Start_date          DATE,
    End_date            DATE,
    Availability_status VARCHAR2(20) DEFAULT 'Available' 
                        CHECK (Availability_status IN ('Available', 'Full', 'Cancelled')),

       CONSTRAINT fk_travel_package_hotel
        FOREIGN KEY (hotel_id)
        REFERENCES Hotel(hotel_id)
);

CREATE TABLE REVIEW (
    review_id          NUMBER(10)       PRIMARY KEY,
    hotel_id           NUMBER(10)       NOT NULL,
    customer_id        NUMBER(10)       NOT NULL,
    apt_id             NUMBER(10)       NOT NULL,
    would_recommend    VARCHAR2(255),
    review_date        DATE,
    cleanliness_score  NUMBER(10),
    service_score      NUMBER(10),
    value_score        NUMBER(10),
    overall_rating     NUMBER(10),

    constraint fk_review_customer
        FOREIGN KEY (customer_id)
        REFERENCES Customer(customer_id),
        
    constraint fk_review_apartment
        FOREIGN KEY (apt_id)
        REFERENCES Apartment(apt_id)


);



//child
CREATE TABLE TRANSPORT (
    transport_id        NUMBER(10) PRIMARY KEY,
    package_id          NUMBER(10) NOT NULL, 
    type                VARCHAR2(20) NOT NULL,
    pickup_location     VARCHAR2(20),
    dropoff_location    VARCHAR2(20),
    pickup_time         TIMESTAMP,
    dropoff_time        TIMESTAMP,
    trip_cost           NUMBER(8,2) CHECK (trip_cost >= 0) NOT NULL,
    service_provider    VARCHAR2(20),
    availability_status VARCHAR2(20) DEFAULT 'On Time' 
                        CHECK (availability_status IN ('On Time', 'Delayed', 'Cancelled')),
    FOREIGN KEY (package_id) REFERENCES TRAVEL_PACKAGE(package_id)
); 

CREATE TABLE ROOM (
    room_id            NUMBER(10)       PRIMARY KEY,
    hotel_id           NUMBER(10),
    room_type          VARCHAR2(50),
    check_in_date      DATE,
    check_out_date     DATE,
    max_guests         NUMBER(10),
    price_per_night    NUMBER(10,2),
    booking_status     VARCHAR2(30),
    payment_status     VARCHAR2(30),
    availability_status VARCHAR2(30),
    CONSTRAINT hotel_id_fk FOREIGN KEY (hotel_id)
    REFERENCES HOTEL (hotel_id)
);

CREATE TABLE FLIGHT_BOOKING(
    booking_id VARCHAR2(20),
    flight_no NUMBER(10),
    customer_id NUMBER(10),
    seat_no VARCHAR2(10),
    booking_date DATE,
    Fare NUMBER(6,2),
    class VARCHAR2(20),
    payment_status VARCHAR2(20),
    booking_status VARCHAR2(20),
    travel_insurance VARCHAR2(20),
    CONSTRAINT booking_id_pk PRIMARY KEY(booking_id),
    CONSTRAINT flight_no_fk FOREIGN KEY (flight_no)
    REFERENCES FLIGHTS (flight_no),
    CONSTRAINT customer_id_fk FOREIGN KEY (customer_id)
    REFERENCES CUSTOMER (customer_id)
);

CREATE TABLE Staff (
    staff_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hotel_id          NUMBER NOT NULL,
    first_name        VARCHAR2(50) NOT NULL,
    last_name         VARCHAR2(50) NOT NULL,
    position          VARCHAR2(50) NOT NULL,
    email             VARCHAR2(100) UNIQUE NOT NULL,
    phone_number      VARCHAR2(20),
    hire_date         DATE DEFAULT SYSDATE,
    employment_status VARCHAR2(20) DEFAULT 'Active' 
                      CHECK (employment_status IN ('Active', 'Inactive', 'On Leave')),
    salary            NUMBER(10,2) CHECK (salary >= 0),
    CONSTRAINT fk_hotel
        FOREIGN KEY (hotel_id)
        REFERENCES Hotel(hotel_id)
        ON DELETE CASCADE
);

CREATE TABLE PACKAGE_TRANSPORT (
    PackageTransport_id    NUMBER(10)PRIMARY KEY,
    package_id             NUMBER(10)NOT NULL,
    transport_id           NUMBER(10)NOT NULL,
    departure_date         DATE,
    arrival_date           DATE,
    departure_time         TIMESTAMP,
    arrival_time           TIMESTAMP,
    pickup_location        VARCHAR2(50),
    dropoff_location       VARCHAR2(50),
    price_included         NUMBER(8,2) CHECK (price_included >= 0),
    FOREIGN KEY (package_id) REFERENCES TRAVEL_PACKAGE(package_id),
    FOREIGN KEY (transport_id) REFERENCES TRANSPORT(transport_id)
);

